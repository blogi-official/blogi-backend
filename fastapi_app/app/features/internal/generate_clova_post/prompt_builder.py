# fastapi_app/app/features/internal/generate_clova_post/prompt_builder.py


def build_prompt(title: str, article_content: str) -> str:
    return f"""다음 키워드를 주제로 블로그 본문을 작성해 주세요.

[작성 조건]
- 전체 글자 수는 공백 제외 3,000자 이상이 되도록 작성해 주세요. ( **반드시 2000자 이상은 되어야함** )
- 제목은 작성하지 마세요. **본문만 작성합니다.**
- 총 5단락 이상 작성하며, **모든 단락은 아래 형식을 반드시 따르세요**:
  - 첫 줄: **소제목 (한 줄)** (반드시 `**소제목**` 형식으로 작성 / 소제목이라고 명시 금지 )
  - 둘째 줄: 본문 내용 (6~9문장, 줄바꿈 없이 연결)
- **모든 단락에 반드시 소제목이 포함되어야 합니다.**
- 각 단락 사이는 빈 줄(\\n\\n)을 넣어 구분해 주세요.


[형식 예시]

** 아이유의 신곡은 고공행진**
이 단락은 예시입니다. 첫 줄은 소제목이고, 다음 줄은 본문입니다. 본문은 6~9문장으로 이어지며, 줄바꿈 없이 연결합니다. 블로그처럼 부드러운 말투로 작성해 주세요.

** 아이유 인기는 어디까지 **
이 단락도 동일한 형식입니다. 모든 단락은 반드시 소제목과 본문 2줄로 구성되어야 합니다. 이런 형식으로 총 5개 이상의 단락을 작성해 주세요.

[키워드]
{title}

[기사 내용]
{article_content}
"""
